package hexanome.fourteen.server.model.board.gem;

/**
 * Gem color.
 */
public enum GemColor {

  /**
   * For identifying the Ruby Tokens.
   */
  RED,

  /**
   * For identifying the Sapphire Tokens.
   */
  BLUE,

  /**
   * For identifying the Emerald Tokens.
   */
  GREEN,

  /**
   * For identifying the Onyx Tokens.
   */
  BLACK,

  /**
   * For identifying the Diamond Tokens.
   */
  WHITE,

  /**
   * For identifying the Gold Joker Tokens.
   */
  GOLD
}
